/**
 * SYCL twin of work_orchestrator_gpu.cc.
 *
 * Submits a persistent single_task kernel that runs WorkerSycl::PollOnce()
 * in a loop until the host writes control->exit_flag = 1. The structure
 * mirrors the CUDA orchestrator with three differences:
 *   1. Single-source SYCL: the persistent kernel is a lambda passed to
 *      sycl::handler::single_task — no separate __global__ entry point.
 *   2. No CUDA Dynamic Parallelism: tasks execute inline within the
 *      persistent kernel (see worker_sycl.h).
 *   3. The "stream" hosted in WorkOrchestrator::stream_ is a heap
 *      sycl::queue allocated by GpuApi::CreateStream(). The opaque
 *      void* representation matches the CUDA path so the host-facing
 *      WorkOrchestrator API is identical.
 */

